/// OOPs : Object Oriented Programming

// OOP is a programming paradigm (way of writing programs),
// where we structure our code using classes and objects.

/// OOPs in Dart
// It is a pure Object Oriented language.
// Everything in Dart is an object (int, double, String, function, etc.)
// OOPs helps to model real-world entities and write clean, reusable, and scalable code.
// Main Purpose : break complex problems into smaller objects

/// Advantages
// Organize large codebases
// Reuse code
// Improve readability
// Maintain & scale Flutter apps easily

